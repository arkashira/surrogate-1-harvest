#!/usr/bin/env python3
"""axentx release — autonomous semver releases per project.

User directive (2026-05-02):
  > "Release feature เองได้ การตัดสินใจ ให้ agent model ที่ resioning ดีๆ
  >  ตัวใหญ่กว่า เป็นคนตัดสินใจ ไม่ต้องมี human in the loop เลย"

Walks each axentx repo every RELEASE_POLL_SEC, counts commits since last
tag. For repos with ≥ MIN_COMMITS new commits:
  1. Bootstrap v0.1.0 if no tags exist (no LLM needed)
  2. call_llm_strong(allow_degrade=True) to classify bump = patch/minor/major
  3. Generate release notes via separate call (failure-tolerant — falls
     back to autogenerated notes from commit log)
  4. git tag + push → gh release create

Conservative defaults: ≥ 5 commits + 4h cadence. No-human-in-loop.

Hardening over the original (2026-04 era):
  * Bootstrap branch — first release on a never-tagged repo no longer
    requires LLM (uses v0.1.0 with auto-notes)
  * Decision split — bump decision (small JSON) and notes generation
    (large markdown) are separate calls. If notes fail, release still
    proceeds with autogenerated notes from commit log.
  * call_llm_strong for the bump verdict (decision-grade gate per user
    directive). Falls through to standard call_llm if every strong
    provider rate-limits.
  * Discord post on every successful release for visibility.
"""
from __future__ import annotations
import datetime
import json
import os
import re
import subprocess
import sys
import urllib.request
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))
from axentx_pipeline import (REPO_ROOT, log, call_llm, call_llm_strong,
                             daemon_loop)

POLL_SEC = int(os.environ.get("RELEASE_POLL_SEC", "3600"))    # 1h (was 4h)
MIN_COMMITS = int(os.environ.get("RELEASE_MIN_COMMITS", "3"))  # was 5
PROJECTS_ROOT = Path(os.environ.get("AXENTX_ROOT", "/opt/axentx"))
PROJECTS = ["Costinel", "vanguard", "airship", "workio", "surrogate-1"]
DISCORD = os.environ.get("DISCORD_WEBHOOK", "")

BUMP_SYSTEM = """Given commit subjects since the last semver tag, decide the
bump kind. Output STRICT JSON only:

{"bump":"patch|minor|major","reason":"<≤120 char rationale>"}

Rules:
- "major" — any breaking change (API removal, config rename, schema bump
  that requires manual migration, file relocation that breaks imports).
- "minor" — at least one new user-visible feature added (new endpoint,
  new daemon, new command, new flag).
- "patch" — bugfixes, refactors, doc, ops, perf, dependency bumps,
  internal renames, test changes only.
"""

NOTES_SYSTEM = """Given commit subjects, write a concise GitHub release
notes markdown body for this version. Group by category (Features,
Fixes, Ops/Infra, Docs, Other). 1 line per commit, no fluff.

Plain markdown only — no JSON wrapping, no preamble.
"""


def git(repo: Path, *args: str, timeout: int = 20) -> subprocess.CompletedProcess:
    """Run git with timeout. Returns a CompletedProcess-shaped object even
    when timeout fires — caller's existing returncode!=0 path handles it
    as 'no result' instead of crashing the whole daemon (which racked up
    1156 restarts on this exact failure mode 2026-05-03)."""
    try:
        return subprocess.run(
            ["git", "-C", str(repo), *args],
            capture_output=True, text=True, timeout=timeout,
        )
    except subprocess.TimeoutExpired:
        # Synthetic non-zero result so caller treats as 'git command failed'
        return subprocess.CompletedProcess(
            args=("git", "-C", str(repo), *args),
            returncode=124, stdout="", stderr=f"timeout {timeout}s",
        )


def bump_version(prev: str, kind: str) -> str:
    try:
        parts = [int(x) for x in prev.lstrip("v").split(".")]
    except Exception:
        parts = [0, 0, 0]
    while len(parts) < 3:
        parts.append(0)
    if kind == "major":
        parts = [parts[0] + 1, 0, 0]
    elif kind == "minor":
        parts = [parts[0], parts[1] + 1, 0]
    else:
        parts[2] += 1
    return f"v{parts[0]}.{parts[1]}.{parts[2]}"


def post_discord(msg: str) -> None:
    if not DISCORD:
        return
    body = json.dumps({"content": msg[:1900]}).encode()
    req = urllib.request.Request(DISCORD, data=body, headers={
        "Content-Type": "application/json",
        "User-Agent": "DiscordBot (axentx/release-daemon, 1.0)",
    })
    try:
        urllib.request.urlopen(req, timeout=8)
    except Exception:
        pass


def parse_json_block(text: str) -> dict:
    txt = text.strip()
    if "```" in txt:
        seg = txt.split("```")[1]
        if seg.startswith("json"):
            seg = seg[4:]
        txt = seg.strip()
    return json.loads(txt)


def autogen_notes(proj: str, last_tag: str, new_tag: str,
                  subjects: list[str]) -> str:
    """Fallback notes when LLM is unavailable — group commits by prefix."""
    by_kind: dict[str, list[str]] = {}
    for s in subjects:
        # Match conventional-commit style: "feat(scope): ..." / "fix: ..."
        m = re.match(r"^([a-z]+)(?:\([^)]+\))?:\s*(.+)$", s)
        kind = (m.group(1) if m else "other").lower()
        body = (m.group(2) if m else s).strip()
        by_kind.setdefault(kind, []).append(body)
    order = ["feat", "fix", "perf", "refactor", "docs", "ops", "ci",
             "chore", "test", "other"]
    lines = [f"# {new_tag} ({proj})", "",
             f"Auto-released: {datetime.datetime.utcnow().date().isoformat()} "
             f"(was {last_tag}; {len(subjects)} commits)", ""]
    for k in order:
        items = by_kind.pop(k, None)
        if not items:
            continue
        title_map = {"feat": "Features", "fix": "Fixes", "perf": "Performance",
                     "refactor": "Refactors", "docs": "Docs",
                     "ops": "Ops/Infra", "ci": "CI", "chore": "Chores",
                     "test": "Tests", "other": "Other"}
        lines.append(f"### {title_map.get(k, k.capitalize())}")
        for it in items[:30]:
            lines.append(f"- {it}")
        lines.append("")
    # Drain remaining categories
    for k, items in by_kind.items():
        lines.append(f"### {k.capitalize()}")
        for it in items[:30]:
            lines.append(f"- {it}")
        lines.append("")
    return "\n".join(lines)


def do_one() -> bool:
    fired = 0
    for proj in PROJECTS:
        repo = PROJECTS_ROOT / proj
        if not (repo / ".git").exists():
            continue
        r = git(repo, "describe", "--tags", "--abbrev=0")
        has_tags = (r.returncode == 0 and r.stdout.strip())
        last_tag = r.stdout.strip() if has_tags else ""

        # Bootstrap path — repo never tagged before, fire v0.1.0 with
        # auto-notes. No LLM, no discretion needed.
        if not has_tags:
            r = git(repo, "log", "--pretty=format:%s")
            subjects = [s for s in r.stdout.splitlines() if s][:50]
            if len(subjects) < MIN_COMMITS:
                continue
            new_tag = "v0.1.0"
            notes = autogen_notes(proj, "(no prior tag)", new_tag, subjects)
            log("release", f"▸ {proj}: bootstrap → {new_tag} ({len(subjects)} commits)")
            if cut_release(proj, repo, new_tag, notes, "(no prior tag)", subjects):
                fired += 1
            continue

        # Incremental path — count commits since last tag
        r = git(repo, "log", f"{last_tag}..HEAD", "--pretty=format:%s")
        subjects = [s for s in r.stdout.splitlines() if s][:50]
        if len(subjects) < MIN_COMMITS:
            continue
        log("release", f"▸ {proj}: {len(subjects)} commits since {last_tag}")

        # Decision-grade bump call
        bump_kind = "patch"
        try:
            bump_prompt = (
                f"Project: {proj}\nLast tag: {last_tag}\n"
                f"Commit subjects ({len(subjects)}):\n" +
                "\n".join(f"- {s}" for s in subjects)
            )
            out = call_llm_strong(bump_prompt, system=BUMP_SYSTEM,
                                  max_tokens=200, timeout=30,
                                  allow_degrade=True)
            d = parse_json_block(out)
            bump_kind = d.get("bump", "patch")
            if bump_kind not in ("patch", "minor", "major"):
                bump_kind = "patch"
            log("release", f"  decision: bump={bump_kind}, reason={d.get('reason','')[:60]}")
        except Exception as e:
            log("release", f"  ⚠ bump-decision LLM fail ({str(e)[:80]}) — defaulting to patch")
            bump_kind = "patch"

        new_tag = bump_version(last_tag, bump_kind)

        # Notes — best-effort, fall through to auto-gen
        notes = ""
        try:
            notes_prompt = (
                f"Project: {proj}  |  {last_tag} → {new_tag}\n\n"
                f"Commit subjects:\n" + "\n".join(f"- {s}" for s in subjects)
            )
            out = call_llm(notes_prompt, system=NOTES_SYSTEM,
                           max_tokens=1500, timeout=45)
            notes = out.strip()
            if "```" in notes:
                notes = notes.replace("```markdown", "").replace("```md", "")
                notes = notes.replace("```", "").strip()
        except Exception as e:
            log("release", f"  ⚠ notes LLM fail — using auto-gen ({str(e)[:80]})")
        if not notes or len(notes) < 60:
            notes = autogen_notes(proj, last_tag, new_tag, subjects)

        if cut_release(proj, repo, new_tag, notes, last_tag, subjects):
            fired += 1

    return fired > 0


def cut_release(proj: str, repo: Path, new_tag: str, notes: str,
                last_tag: str, subjects: list[str]) -> bool:
    notes_path = repo / f".axentx-release-{new_tag}.md"
    notes_path.write_text(notes if notes.endswith("\n") else notes + "\n")
    # tag + push
    tg = git(repo, "tag", "-a", new_tag, "-m", f"axentx release {new_tag}")
    if tg.returncode != 0:
        log("release", f"  ⚠ {proj} tag fail: {tg.stderr[:120]}")
        return False
    push = git(repo, "push", "origin", new_tag, timeout=60)
    if push.returncode != 0:
        log("release", f"  ⚠ {proj} push tag fail: {push.stderr[:120]}")
        return False
    # GitHub Release via gh
    try:
        gh = subprocess.run(
            ["gh", "release", "create", new_tag,
             "--repo", f"axentx/{proj}",
             "-F", str(notes_path),
             "--title", new_tag],
            capture_output=True, text=True,
            cwd=str(repo), timeout=30,
        )
        if gh.returncode != 0:
            log("release", f"  ⚠ {proj} gh release fail: {(gh.stderr or '')[:200]}")
            # Tag still exists on remote — partial success
            return False
        log("release", f"  ✓ {proj} {last_tag or 'init'} → {new_tag} "
                       f"({len(subjects)} commits, gh release created)")
        post_discord(
            f"🚀 **axentx release** `{proj}` {last_tag or 'init'} → **{new_tag}** "
            f"({len(subjects)} commits)\n"
            f"https://github.com/axentx/{proj}/releases/tag/{new_tag}"
        )
        return True
    except Exception as e:
        log("release", f"  ⚠ {proj} gh release fail: {e}")
        return False


if __name__ == "__main__":
    daemon_loop("release", POLL_SEC, do_one)
